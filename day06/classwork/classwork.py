# სამი განსხვავებული მონაცემთა ტიპის ცვლადი
name = "ჯეკო"           # string (ტექსტი)
age = 19                  # integer (მთელი რიცხვი)
height = 1.81            # float (ათწილადი რიცხვი)

# თითოეული ცვლადის მნიშვნელობისა და მონაცემთა ტიპის დაბეჭდვა
print("სახელი:", name, "| ტიპი:", type(name))
print("ასაკი:", age, "| ტიპი:", type(age))
print("სიმაღლე:", height, "| ტიპი:", type(height))
