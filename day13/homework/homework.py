# 4. განსხვავება != და ==
# == და != შედარების ოპერატორები
print("\n4. განსხვავება != და ==:")

# == შეადარებს, რომ ორი მნიშვნელობა იყოს თანაბარი
print("== შედარება: 10 == 10 არის", 10 == 10)  # True, რადგან 10 და 10 თანაბარია
print("== შედარება: 10 == 20 არის", 10 == 20)  # False, რადგან 10 და 20 არ არის თანაბარი

# != შეადარებს, რომ ორი მნიშვნელობა არ იყოს თანაბარი
print("!= შედარება: 10 != 20 არის", 10 != 20)  # True, რადგან 10 და 20 არ არის თანაბარი
print("!= შედარება: 10 != 10 არის", 10 != 10)  # False, რადგან 10 და 10 თანაბარია


