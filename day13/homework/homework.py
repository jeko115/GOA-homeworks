# 1. მომხმარებლისგან რიცხვების მიღება და მათზე მათემატიკური ოპერაციები
num1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
num2 = float(input("შეიყვანეთ მეორე რიცხვი: "))

# მათემატიკური ოპერაციები
addition = num1 + num2
subtraction = num1 - num2
multiplication = num1 * num2

# გაყოფა - თუ num2 არის ნული, გავამჟღავნებთ შეცდომას
if num2 != 0:
    division = num1 / num2
else:
    division = "არ არის შესაძლებელი გაყოფა ნულის მიხედვით"

# შედეგების გამოტანა
print(f"\n1. მათემატიკური ოპერაციები:")
print(f"დამატება: {addition}")
print(f"გამოტანა: {subtraction}")
print(f"გამრავლება: {multiplication}")
print(f"გაყოფა: {division}")


