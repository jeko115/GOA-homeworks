# 2. პირველი ოთხი ელემენტი
print(f"პირველი ოთხი ელემენტი: {fruits[:4]}")

# 3. ბოლო სამი ელემენტი
print(f"ბოლო სამი ელემენტი: {fruits[-3:]}")




# ქალაქების სია
cities = ["თბილისი", "ბათუმი", "ქუთაისი", "რუსთავი", "გორი", "ზუგდიდი"]

# 1. მეორე და მეექვსე ელემენტები
print(f"მეორე ელემენტი: {cities[1]}")
print(f"მეექვსე ელემენტი: {cities[5]}")

# 2. შუა ორი ელემენტი
print(f"შუა ორი ელემენტი: {cities[2:4]}")

# 3. არეული თანმიმდევრობა
import random
random.shuffle(cities)
print(f"არეული სია: {cities}")







# ჯგუფელების სახელების სია
groupmates = ["ნიკა", "ანა", "გიორგი", "მარი", "ლაშა", "სოფო"]

