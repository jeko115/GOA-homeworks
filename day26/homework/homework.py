# ხილის სია
fruits = ["ვაშლი", "მსხალი", "ატამი", "ქლიავი", "ბალი", "ატამი", "კივი", "ბანანი"]

# 1. მესამე და მეშვიდე ელემენტები
print(f"მესამე ელემენტი: {fruits[2]}")
