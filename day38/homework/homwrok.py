def number_to_string(n):
    return str(n)

# Examples
print(number_to_string(123))   # "123"
print(number_to_string(999))   # "999"
print(number_to_string(-100))  # "-100"




def repeat_string(n, s):
    return s * n

# Examples
print(repeat_string(6, "I"))      # "IIIIII"
print(repeat_string(5, "Hello"))  # "HelloHelloHelloHelloHello"



def string_to_number(s):
    return int(s)

# Examples
print(string_to_number("1234"))  # 1234
print(string_to_number("605"))   # 605
print(string_to_number("1405"))  # 1405
print(string_to_number("-7"))    # -7
