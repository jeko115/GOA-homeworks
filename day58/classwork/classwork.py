def calculate_difference(a, b):
    # თუ a ლუწია (იყოფა 2-ზე ნაშთის გარეშე)
    if a % 2 == 0:
        a = a // 2  # ლუწ შემთხვევაში გაყავით 2-ზე
    else:
        a = a + 1   # კენტ შემთხვევაში დაამატეთ 1

    return a - b  # აბრუნებს a და b-ის სხვაობას

# ▶️ ფუნქციის გამოძახება:
result = calculate_difference(6, 2)
print("შედეგი:", result)  # უნდა დაბეჭდოს 1 (6 ლუწია → 6//2 = 3 → 3 - 2 = 1)
